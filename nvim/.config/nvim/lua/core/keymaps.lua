-- ===============================
-- キーマップ設定
-- ===============================

vim.g.mapleader = " "

local map = vim.keymap.set

-- --- 移動系（通常モード） ---
map("n", "K", "10k")
map("n", "J", "10j")
map("n", "H", "0")
map("n", "L", "$")

-- --- 移動系（ビジュアルモード） ---
map("v", "K", "10k")
map("v", "J", "10j")
map("v", "H", "0")
map("v", "L", "$")

-- --- 矩形選択 ---
map("n", "<leader>V", "<C-v>")

-- --- インサートモード脱出 ---
map("i", "jj", "<ESC>")

-- --- d$ / d0 のショートカット ---
map("n", "dl", "d$")
map("n", "dh", "d0")

-- --- 検索ハイライト解除 ---
map("n", "<Esc><Esc>", ":nohlsearch<CR>")
